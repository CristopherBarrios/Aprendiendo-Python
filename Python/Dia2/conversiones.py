def convertir_centigrados_farenheit(num1, num2, num3):#funcion con 3 numeros
    resultado = num1 * num2 + num3#formula para la convercion
    return resultado

def convertir_farenheit_centigrados(num1, num2, num3):#funcion con 3 numeros
    resultado = num1 - num2
    resultado2 = resultado * num3#formula para la convercion
    return resultado2
